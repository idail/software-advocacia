    function atualiza_pagamentos_vencer() {
        debugger;

        $.ajax({
            url: "http://localhost/software-advocacia/API/PagamentoAPI.php",
            type: "get",
            dataType: "json",
            data: {
                processo_pagamento: "buscar_pagamentos_vencidos",
            },
            success: function(retorno) {
                if (retorno.length > 0)
                {
                    $("#visualiza-pagamentos-vencidos").html(retorno.length);
                    $("#numeracao-pagamentos-vencer").html(retorno.length);
                }else
                {
                    $("#numeracao-pagamentos-vencer").html(retorno.length);
                    $("#visualiza-pagamentos-vencidos").html(retorno.length);
                }
            },
            error: function(xhr, status, error) {
                $("#corpo-mensagem-falha-busca-mensagem-pagamento-vencido").html("Falha ao buscar pagamentos vencidos:" + error);
                $("#mensagem-falha-busca-mensagem-pagamento-vencido").show();
                $("#mensagem-falha-busca-mensagem-pagamento-vencido").fadeOut(4000);
            },
        });


    }

    $("#visualizar-pagamentos-vencer").click(function(e) {
        e.preventDefault();

        $.ajax({
            url: "http://localhost/software-advocacia/API/PagamentoAPI.php",
            type: "get",
            dataType: "json",
            data: {
                processo_pagamento: "buscar_pagamentos_vencidos",
            },
            success: function(retorno) {
                debugger;

                if (retorno.length > 0) {
                    let recebe_tabela_pagamento = document.querySelector(
                        "#registro-pagamento-vencido"
                    );

                    $("#registro-pagamento-vencido").html("");

                    for (let indice = 0; indice < retorno.length; indice++) {
                        let objeto_data = new Date(retorno[indice].data_vencimento_pagamento);
                        let data_formatada_brasileiro = objeto_data.toLocaleDateString('pt-BR');

                        recebe_tabela_pagamento.innerHTML +=
                            "<tr>" +
                            "<td>" + data_formatada_brasileiro + "</td>" +
                            "<td>" + retorno[indice].servico_realizado_pagamento + "</td>" +
                            "<td>" + retorno[indice].nome_cliente + "</td>" +
                            "<td>Vencido</td>" +
                            "</tr>";
                    }
                    $("#registro-pagamento-vencido").append(recebe_tabela_pagamento);
                } else {
                    $("#registro-pagamento-vencido").html("");
                    $("#registro-pagamento-vencido").append("<td colspan='4' class='text-center'>Nenhum registro localizado</td>");
                }
            },
            error: function(xhr, status, error) {

            },
        });

        $("#pagamentos-vencidos").modal("show");
    });
